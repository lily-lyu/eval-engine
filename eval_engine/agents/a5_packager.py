from pathlib import Path
from typing import Any, Dict, List, Optional

from ..core.storage import append_jsonl, write_json
from ..core.schema import validate_or_raise
from ..core.timeutil import now_iso
from ..core.metrics import compute_slice_metrics


def package_run(
    run_dir: Path,
    spec: Dict[str, Any],
    items: List[Dict[str, Any]],
    oracles: List[Dict[str, Any]],
    eval_results: List[Dict[str, Any]],
    clusters: List[Dict[str, Any]],
    action_plans: List[Dict[str, Any]],
    attempted_total: Optional[int] = None,
    item_abort_total: Optional[int] = None,
    latency_ms_list: Optional[List[Optional[int]]] = None,
    data_requests: Optional[List[Dict[str, Any]]] = None,
    release_manifest: Optional[List[Dict[str, Any]]] = None,
) -> None:
    # Validate records before packaging
    for it in items:
        validate_or_raise("item.schema.json", it)
    for oc in oracles:
        validate_or_raise("oracle.schema.json", oc)
    for er in eval_results:
        validate_or_raise("eval_result.schema.json", er)
    for cl in clusters:
        validate_or_raise("failure_cluster.schema.json", cl)
    for ap in action_plans:
        validate_or_raise("action_plan.schema.json", ap)

    # Emit JSONLs (clusters = analytical; action_plans = operational)
    append_jsonl(run_dir / "released_items.jsonl", items)
    append_jsonl(run_dir / "released_oracles.jsonl", oracles)
    append_jsonl(run_dir / "eval_results.jsonl", eval_results)
    append_jsonl(run_dir / "clusters.jsonl", clusters)
    append_jsonl(run_dir / "action_plans.jsonl", action_plans)

    if data_requests is not None:
        append_jsonl(run_dir / "data_requests.jsonl", data_requests)

    if release_manifest is not None:
        for row in release_manifest:
            out = dict(row)
            if out.get("released_at") == "":
                out["released_at"] = now_iso()
            append_jsonl(run_dir / "release_manifest.jsonl", [out])

    # Run summary
    total = len(items)
    qa_passed = sum(1 for er in eval_results)  # because we only evaluate qa-passed in this MVP
    eval_passed = sum(1 for er in eval_results if er["verdict"] == "pass")
    failures = qa_passed - eval_passed

    summary = {
        "dataset_name": spec["dataset_name"],
        "dataset_spec_version": spec["dataset_spec_version"],
        "created_at": now_iso(),
        "counts": {
            "items_total": total,
            "qa_passed_evaluated": qa_passed,
            "eval_passed": eval_passed,
            "eval_failed": failures
        }
    }

    if attempted_total is not None and item_abort_total is not None:
        slice_result = compute_slice_metrics(
            items, oracles, eval_results,
            attempted_total=attempted_total,
            item_abort_total=item_abort_total,
            latency_ms_list=latency_ms_list,
        )
        summary["slice_metrics"] = slice_result["slice_metrics"]

    if data_requests is not None:
        summary["data_requests"] = data_requests
        summary["data_requests_count"] = len(data_requests)
        by_issue = {}
        for r in data_requests:
            it = r["issue_type"]
            by_issue[it] = by_issue.get(it, 0) + 1
        summary["data_requests_by_issue_type"] = by_issue

    write_json(run_dir / "run_summary.json", summary)

    # Data card JSON (minimal MVP)
    data_card = {
        "dataset_name": spec["dataset_name"],
        "dataset_spec_version": spec["dataset_spec_version"],
        "motivation": "MVP eval dataset generated by eval-engine pipeline for deterministic, schema-first evaluation.",
        "intended_use": "Regression testing, capability tracking, and failure clustering for model/app iteration.",
        "composition": f"{total} synthetic items across domains {spec['allowed_domain_tags']}.",
        "limitations": "MVP only supports deterministic JSON tasks and simple exact_match grading.",
        "version_history": "1.0.0: initial MVP schema-first pipeline."
    }
    validate_or_raise("data_card.schema.json", data_card)
    write_json(run_dir / "data_card.json", data_card)
